public class InviteHistory {
        private String name;
        private int imageId;


        public InviteHistory(String name, int imageId){
            this.name=name;
            this.imageId=imageId;
        }
        public String getName(){
            return name;
        }

        public int getImageId() {
            return imageId;
        }
    }


